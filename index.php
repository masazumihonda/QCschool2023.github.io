<h2> On this school </h2>
<p>
近年、量子コンピュータ並びにその周辺の理論的・技術的発展により、素粒子・
原子核分野においても「場の理論の新しい計算方法」に注目が集まっています。
<br><br>
特にゲート型量子計算機をターゲットとする量子アルゴリズムやテンソルネットワーク法による計算法は、行列模型・量子重力・量子エンタングルメント・符号問題の生じる場の理論など、従来の計算法では困難があるために、その物理現象が未だ不明な理論に対する新たな計算法として有望視されています。<br><br>
このスクールでは、大学院生とポスドクを主な対象とし、量子計算とテンソルネットワーク分野の日本語による講義および参加者による発表を実施します。
